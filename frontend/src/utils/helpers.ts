export function genreColor(genre: string | null): string {
  if (!genre) return ''
  const map: Record<string, string> = {
    Shooter: '',
    MOBA: 'blue',
    'Battle Royale': 'purple',
    RPG: 'red',
    MMO: 'green',
    Horror: 'red',
  }
  return map[genre] ?? ''
}


